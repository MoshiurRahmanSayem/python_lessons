
# Lesson 33: Packages
# This program demonstrates how to create and import a package with multiple modules.
# In this example, we have a package named 'mypackage' that contains two modules: 'module1.py' and 'module2.py'.
# Each module has its own function.
from mypackage import module1, module2

print(module1.greet("Abul"))
print(module2.farewell("Kabul"))

